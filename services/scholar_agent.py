"""SCHOLAR — академический агент AI Zavod.

Научные публикации, грантовые заявки, литобзоры,
оформление по ГОСТ/ВАК, подготовка презентаций для защиты.
"""
from __future__ import annotations

import logging

from services.base_agent import BaseAgent

logger = logging.getLogger("aizavod.scholar")


class ScholarAgent(BaseAgent):
    agent_name = "scholar"
    model_env_var = "SCHOLAR_MODEL"
    default_temperature = 0.3
    default_max_tokens = 3000

    system_prompt = (
        "Ты — SCHOLAR, академический агент платформы AI Zavod. "
        "Помогаешь с научными публикациями, грантовыми заявками, "
        "литературными обзорами и оформлением по ГОСТ/ВАК. "
        "Пишешь научным стилем, но понятно. Ссылаешься на реальные "
        "методологии и подходы. Формат: РНФ, ФСИ, Сколково."
    )

    async def write_grant(self, topic: str, fund: str, budget: str = "") -> str:
        """Подготовить черновик грантовой заявки."""
        prompt = f"""Подготовь черновик грантовой заявки.

ТЕМА: {topic}
ФОНД: {fund}
БЮДЖЕТ: {budget or 'не указан'}

Структура заявки:
1. **Название проекта** (ёмкое, научное)
2. **Аннотация** (до 500 слов)
3. **Научная новизна** — чем это отличается от существующих решений
4. **Цели и задачи** — конкретные, измеримые
5. **Методы исследования** — какие подходы используются
6. **Ожидаемые результаты** — что получим
7. **Практическая значимость** — кому это нужно
8. **План работ** — по этапам (кварталы)
9. **Ключевые слова** — для классификации"""
        return await self._call_llm(prompt, max_tokens=4000)

    async def literature_review(self, topic: str, scope: str = "Россия и мир") -> str:
        """Литературный обзор по теме."""
        prompt = f"""Составь структурированный литературный обзор.

ТЕМА: {topic}
ОХВАТ: {scope}

Структура:
1. **Введение** — актуальность проблемы
2. **Текущее состояние** — что уже сделано в мире
3. **Российский контекст** — что сделано в РФ
4. **Пробелы в исследованиях** — что не изучено
5. **Перспективные направления** — куда движется область
6. **Ключевые работы** — авторы и подходы (описательно)

Пиши научным стилем, но без воды."""
        return await self._call_llm(prompt, max_tokens=4000)

    async def format_article(self, draft: str, standard: str = "ГОСТ Р 7.0.5-2008") -> str:
        """Оформить статью по стандарту."""
        prompt = f"""Оформи научную статью по стандарту {standard}.

ЧЕРНОВИК:
{draft[:4000]}

Сделай:
1. Структурируй по разделам (введение, методы, результаты, выводы)
2. Оформи библиографические ссылки по {standard}
3. Добавь аннотацию и ключевые слова (рус + англ)
4. Проверь научный стиль изложения
5. Предложи журналы ВАК/РИНЦ для публикации"""
        return await self._call_llm(prompt, max_tokens=4000)

    async def research_question(self, question: str) -> str:
        """Ответ на исследовательский вопрос."""
        prompt = f"""Ответь на исследовательский вопрос как учёный.

ВОПРОС: {question}

Структура:
1. **Ответ** — краткий и точный
2. **Обоснование** — на чём основано
3. **Методология** — как это исследуется
4. **Текущий консенсус** — что принято в научном сообществе
5. **Открытые вопросы** — что ещё не ясно"""
        return await self._call_llm(prompt)


_agent: ScholarAgent | None = None


def get_scholar_agent() -> ScholarAgent:
    global _agent
    if _agent is None:
        _agent = ScholarAgent()
    return _agent
