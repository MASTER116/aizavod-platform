"""DARWIN — модуль самообучения и оптимизации агентов AI Zavod.

Анализирует результаты работы агентов, собирает фидбэк,
предлагает улучшения промптов и конфигурации.
"""
from __future__ import annotations

import logging

from services.base_agent import BaseAgent

logger = logging.getLogger("aizavod.darwin")


class DarwinAgent(BaseAgent):
    agent_name = "darwin"
    model_env_var = "DARWIN_MODEL"
    default_temperature = 0.4
    default_max_tokens = 3000

    system_prompt = (
        "Ты — DARWIN, модуль самообучения мультиагентной платформы AI Zavod. "
        "Твоя задача — анализировать качество работы агентов, находить паттерны "
        "ошибок и предлагать конкретные улучшения. Ты работаешь как внутренний "
        "QA-инженер и методолог. Будь конкретен — предлагай изменения в промптах, "
        "параметрах, маршрутизации."
    )

    async def analyze_response(self, agent_name: str, query: str, response: str) -> str:
        """Анализ качества ответа агента."""
        prompt = f"""Проанализируй ответ агента на запрос пользователя.

АГЕНТ: {agent_name}
ЗАПРОС: {query}
ОТВЕТ АГЕНТА:
{response[:3000]}

Оцени по критериям:
1. **Релевантность** (0-10): отвечает ли на вопрос?
2. **Полнота** (0-10): всё ли раскрыто?
3. **Точность** (0-10): нет ли ошибок или галлюцинаций?
4. **Структура** (0-10): легко ли читать?
5. **Действенность** (0-10): может ли пользователь действовать по ответу?

ИТОГО: средний балл /10
ПРОБЛЕМЫ: что конкретно улучшить
ПРЕДЛОЖЕНИЕ: конкретное изменение в промпте/настройках агента"""
        return await self._call_llm(prompt)

    async def optimize_prompt(self, agent_name: str, current_prompt: str, issues: str) -> str:
        """Предложить улучшенный промпт для агента."""
        prompt = f"""Улучши системный промпт агента.

АГЕНТ: {agent_name}
ТЕКУЩИЙ ПРОМПТ:
{current_prompt}

ВЫЯВЛЕННЫЕ ПРОБЛЕМЫ:
{issues}

Верни:
1. **Улучшенный промпт** (полностью, готовый к копированию)
2. **Что изменено** и почему
3. **Ожидаемый эффект** на качество ответов"""
        return await self._call_llm(prompt, max_tokens=3000)

    async def weekly_report(self, stats: str) -> str:
        """Еженедельный отчёт по качеству системы."""
        prompt = f"""Составь еженедельный отчёт по качеству работы AI Zavod.

СТАТИСТИКА:
{stats}

Структура отчёта:
1. **Общая оценка** — как работала система на этой неделе
2. **Лучшие агенты** — кто справлялся лучше всех
3. **Проблемные места** — где были сбои/низкое качество
4. **Рекомендации** — конкретные действия для улучшения
5. **Приоритет на следующую неделю** — что исправить первым"""
        return await self._call_llm(prompt)

    async def detect_patterns(self, logs: str) -> str:
        """Выявить паттерны в логах для улучшения маршрутизации."""
        prompt = f"""Проанализируй логи маршрутизации CONDUCTOR и найди паттерны.

ЛОГИ:
{logs[:4000]}

Найди:
1. **Частые ошибки маршрутизации** — запросы, попавшие не к тому агенту
2. **Недостающие ключевые слова** — что добавить для быстрой маршрутизации
3. **Перекрывающиеся зоны** — какие агенты конфликтуют за одинаковые запросы
4. **Новые потребности** — запросы, для которых нет подходящего агента"""
        return await self._call_llm(prompt)


_agent: DarwinAgent | None = None


def get_darwin_agent() -> DarwinAgent:
    global _agent
    if _agent is None:
        _agent = DarwinAgent()
    return _agent
