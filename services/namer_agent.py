"""NAMER — полный цикл нейминга: генерация, проверка доменов, ТЗ, ЕГРЮЛ."""
from __future__ import annotations

import logging

from services.base_agent import BaseAgent

logger = logging.getLogger("aizavod.namer")


class NamerAgent(BaseAgent):
    agent_name = "namer"
    model_env_var = "NAMER_MODEL"
    default_temperature = 0.7  # креативность для генерации названий
    default_max_tokens = 2500

    system_prompt = (
        "Ты — NAMER, агент нейминга платформы AI Zavod. "
        "Генерируешь уникальные, запоминающиеся названия для продуктов, "
        "компаний, агентов, доменов. Проверяешь на конфликты: домены, "
        "товарные знаки ФИПС, ЕГРЮЛ, соцсети. Учитываешь РФ + СНГ "
        "рынок, кириллицу и латиницу. Названия должны быть легко "
        "произносимыми, запоминаемыми и масштабируемыми."
    )

    async def generate_names(self, brief: str, count: int = 15) -> str:
        """Генерация вариантов названий по брифу."""
        prompt = f"""Сгенерируй {count} вариантов названий.

БРИФ: {brief}

Требования:
- Легко произносится на русском и английском
- Запоминается с первого раза
- Нет негативных ассоциаций (РФ + СНГ + международно)
- Масштабируется (не привязано к одному продукту)
- Доменопригодно

Стили (по 2-3 варианта каждого):
1. **Неологизм** — новое слово
2. **Метафора** — образное
3. **Аббревиатура** — из ключевых слов
4. **Составное** — два слова в одно
5. **Кириллица** — если уместно

Для каждого варианта:
- Название (лат + кир если нужно)
- Домен (.ru / .com)
- Почему хорошее название
- Возможные проблемы"""
        return await self._call_llm(prompt, max_tokens=3000)

    async def check_availability(self, name: str) -> str:
        """Проверка доступности названия по всем каналам."""
        prompt = f"""Оцени доступность названия "{name}" по всем каналам.

Проверь (на основе знаний о типичных паттернах):
1. **Домены**: {name}.ru, {name}.com, {name}.рф — вероятно свободны?
2. **ФИПС/Роспатент**: есть ли похожие товарные знаки в классах 9, 35, 42 МКТУ?
3. **ЕГРЮЛ**: есть ли ООО/ИП с похожим названием в IT/AI сфере?
4. **Соцсети**: @{name} в Telegram, VK, GitHub, Instagram — вероятно свободны?
5. **Фонетика**: как звучит, нет ли двусмысленности
6. **Международность**: понятно ли за пределами РФ

ВАЖНО: укажи, что для точной проверки нужен веб-поиск по ФИПС, Whois и ЕГРЮЛ.
Дай предварительную оценку на основе общих знаний.

Итог: РЕКОМЕНДУЕТСЯ / ТРЕБУЕТ ПРОВЕРКИ / НЕ РЕКОМЕНДУЕТСЯ"""
        return await self._call_llm(prompt)

    async def full_naming(self, brief: str) -> str:
        """Полный цикл: генерация + предварительная оценка + финалисты."""
        prompt = f"""Проведи полный цикл нейминга.

БРИФ: {brief}

Этапы:
1. **Анализ ниши** — какие названия уже заняты, какие стили популярны
2. **Генерация** — 15 вариантов в разных стилях
3. **Первичный фильтр** — убери очевидно занятые или проблемные
4. **Финалисты** — 5 лучших с полной картой:
   - Домен .ru / .com (оценка доступности)
   - Товарный знак (оценка конфликтов)
   - Соцсети (оценка доступности)
   - Сильные стороны
   - Риски
5. **Рекомендация** — один лучший вариант с обоснованием"""
        return await self._call_llm(prompt, max_tokens=4000)


_agent: NamerAgent | None = None


def get_namer_agent() -> NamerAgent:
    global _agent
    if _agent is None:
        _agent = NamerAgent()
    return _agent
