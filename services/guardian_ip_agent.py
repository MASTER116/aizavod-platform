"""GUARDIAN-IP — патентный поиск и защита интеллектуальной собственности."""
from __future__ import annotations

import logging

from services.base_agent import BaseAgent

logger = logging.getLogger("aizavod.guardian_ip")


class GuardianIPAgent(BaseAgent):
    agent_name = "guardian_ip"
    model_env_var = "GUARDIAN_IP_MODEL"
    default_temperature = 0.2  # точность для юридических проверок
    default_max_tokens = 3000

    system_prompt = (
        "Ты — GUARDIAN-IP, агент защиты интеллектуальной собственности "
        "платформы AI Zavod. Проверяешь товарные знаки (ФИПС, WIPO), "
        "патенты (ЕАПО, Роспатент), ЕГРЮЛ, домены. "
        "Работаешь с российским и международным IP-правом. "
        "Предупреждаешь о конфликтах ДО запуска продукта/бренда. "
        "Классы МКТУ для AI Zavod: 9, 35, 42."
    )

    async def check_trademark(self, name: str, classes: str = "9, 35, 42") -> str:
        """Проверка товарного знака по базам."""
        prompt = f"""Проведи предварительную проверку товарного знака.

НАЗВАНИЕ: {name}
КЛАССЫ МКТУ: {classes}

Проверь (на основе знаний):
1. **ФИПС/Роспатент** — есть ли зарегистрированные ТЗ с похожим названием в указанных классах
2. **WIPO Madrid Monitor** — международные ТЗ
3. **Фонетическое сходство** — похожие по звучанию ТЗ
4. **Визуальное сходство** — если есть логотип
5. **Семантическое сходство** — по смыслу

Оценка:
- **Риск конфликта**: низкий / средний / высокий
- **Рекомендация**: регистрировать / доработать / отказаться
- **Стоимость регистрации ТЗ в РФ**: ~38,500₽ (пошлина)
- **Следующие шаги**: что сделать для точной проверки

ВАЖНО: укажи, что для точной проверки необходим поиск по ФИПС онлайн."""
        return await self._call_llm(prompt)

    async def check_patent(self, invention: str) -> str:
        """Проверка патентоспособности изобретения."""
        prompt = f"""Проведи предварительную оценку патентоспособности.

ИЗОБРЕТЕНИЕ: {invention}

Оцени:
1. **Новизна** — есть ли аналоги (на основе знаний)
2. **Изобретательский уровень** — неочевидность решения
3. **Промышленная применимость** — можно ли использовать
4. **Тип охраны**: изобретение / полезная модель / промышленный образец / ПО
5. **Базы для проверки**: Роспатент, ЕАПО, Google Patents, Espacenet
6. **Стоимость патентования** в РФ
7. **Сроки** получения патента
8. **Риски**: что может помешать

Для ПО: в РФ программы не патентуются, но можно зарегистрировать в Реестре ПО (Роспатент)."""
        return await self._call_llm(prompt)

    async def ip_audit(self, company_description: str) -> str:
        """Полный IP-аудит компании/продукта."""
        prompt = f"""Проведи IP-аудит.

ОПИСАНИЕ: {company_description}

Проверь:
1. **Товарный знак** — зарегистрирован ли, нет ли конфликтов
2. **Домены** — все ли нужные куплены (.ru, .com, .рф)
3. **Код** — как защищён (лицензия, авторское право)
4. **Контент** — авторские права на тексты, изображения, видео
5. **Данные** — кому принадлежат данные пользователей (152-ФЗ)
6. **Сотрудники** — NDA, соглашения о передаче IP

Рекомендации:
- Что защитить первым (приоритет)
- Стоимость защиты
- Риски при отсутствии защиты"""
        return await self._call_llm(prompt, max_tokens=3500)

    async def domain_analysis(self, domain: str) -> str:
        """Анализ домена: владелец, история, риски."""
        prompt = f"""Проанализируй домен.

ДОМЕН: {domain}

Что нужно проверить (инструкция):
1. **Whois** — кто владеет, когда зарегистрирован, когда истекает
2. **Текущий контент** — что на сайте сейчас
3. **История** — web.archive.org
4. **Владелец** — ЕГРЮЛ если юрлицо
5. **Конфликты** — пересекается ли деятельность владельца с нашей

Если домен занят:
- Варианты: альтернативные домены
- Выкуп: типичная стоимость
- Оспаривание: есть ли основания

ВАЖНО: для точной проверки нужен Whois-сервис. Дай инструкцию и общую оценку."""
        return await self._call_llm(prompt)


_agent: GuardianIPAgent | None = None


def get_guardian_ip_agent() -> GuardianIPAgent:
    global _agent
    if _agent is None:
        _agent = GuardianIPAgent()
    return _agent
