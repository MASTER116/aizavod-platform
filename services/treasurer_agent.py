"""TREASURER — монетизация инфраструктуры и поиск источников дохода.

Survival-режим: проект кормит себя сам с ранних стадий.
Анализ расходов, поиск способов заработка, оптимизация затрат.
"""
from __future__ import annotations

import logging

from services.base_agent import BaseAgent

logger = logging.getLogger("aizavod.treasurer")


class TreasurerAgent(BaseAgent):
    agent_name = "treasurer"
    model_env_var = "TREASURER_MODEL"
    default_temperature = 0.3
    default_max_tokens = 2500

    system_prompt = (
        "Ты — TREASURER, финансовый агент платформы AI Zavod. "
        "Задача — обеспечить выживание проекта: найти источники дохода, "
        "оптимизировать расходы, спланировать денежный поток. "
        "Приоритет: что ближе к деньгам — то первое. "
        "Контекст: стартап из РФ, бюджет ~0, основатель работает "
        "на основной работе, Claude — основной исполнитель. "
        "НЗ 4.5 млн руб — НЕ ТРОГАТЬ ни при каких обстоятельствах."
    )

    async def analyze_expenses(self, expenses: str) -> str:
        """Анализ текущих расходов и рекомендации по оптимизации."""
        prompt = f"""Проанализируй расходы и предложи оптимизацию.

ТЕКУЩИЕ РАСХОДЫ:
{expenses}

Структура:
1. **Разбивка по категориям** — инфраструктура, подписки, сервисы
2. **Обязательные** — без чего проект не работает
3. **Оптимизируемые** — можно заменить на бесплатные или дешевле
4. **Убираемые** — не приносят ценности
5. **Рекомендации** — конкретные альтернативы с ценами
6. **Целевой бюджет** — сколько должно стоить в идеале"""
        return await self._call_llm(prompt)

    async def find_income_sources(self, skills: str, resources: str) -> str:
        """Поиск способов заработка из текущих ресурсов."""
        prompt = f"""Найди способы заработка из текущих ресурсов.

НАВЫКИ: {skills}
РЕСУРСЫ: {resources}

Ранжируй по скорости выхода на деньги:
1. **Немедленно (дни):** что можно продать/сделать прямо сейчас
2. **Быстро (недели):** фриланс, микро-сервисы, консалтинг
3. **Средне (1-3 мес):** продукты, подписки, курсы
4. **Долго (3-6 мес):** гранты, enterprise-продажи

Для каждого:
- Что конкретно делать
- Ожидаемый доход (диапазон)
- Время на запуск
- Платформа/канал
- Риски"""
        return await self._call_llm(prompt, max_tokens=3000)

    async def cash_flow_plan(self, income: str, expenses: str, period: str = "6 месяцев") -> str:
        """Планирование денежного потока."""
        prompt = f"""Составь план денежного потока на {period}.

ДОХОДЫ: {income}
РАСХОДЫ: {expenses}

План:
1. **Текущий баланс** — доходы vs расходы
2. **Прогноз по месяцам** — когда выход в ноль
3. **Критические точки** — когда могут кончиться деньги
4. **Сценарии**: оптимистичный / базовый / пессимистичный
5. **Действия** — что сделать для улучшения каждого сценария
6. **Метрика** — burn rate, runway, break-even

ОГРАНИЧЕНИЕ: НЗ 4.5 млн руб НЕПРИКОСНОВЕННЫ."""
        return await self._call_llm(prompt)

    async def pricing_strategy(self, product: str, market: str = "РФ") -> str:
        """Стратегия ценообразования для продукта."""
        prompt = f"""Разработай стратегию ценообразования.

ПРОДУКТ: {product}
РЫНОК: {market}

Учти:
1. **Конкуренты** — как цены у них (типичные диапазоны)
2. **Себестоимость** — сколько стоит обслужить 1 клиента (API, инфра)
3. **Тарифы** — FREE / STARTER / PRO / ENTERPRISE
4. **Психология цен** — 4,990₽ vs 5,000₽
5. **Конверсия** — ожидаемая при разных ценах
6. **Unit-экономика** — LTV, CAC, маржа
7. **Стратегия входа** — демпинг через бесплатное vs premium-позиционирование"""
        return await self._call_llm(prompt)


_agent: TreasurerAgent | None = None


def get_treasurer_agent() -> TreasurerAgent:
    global _agent
    if _agent is None:
        _agent = TreasurerAgent()
    return _agent
