"""VOICE — голосовой агент AI Zavod.

Подготовка скриптов для звонков, генерация текстов для TTS,
сценарии деловых и рутинных звонков.
"""
from __future__ import annotations

import logging

from services.base_agent import BaseAgent

logger = logging.getLogger("aizavod.voice")


class VoiceAgent(BaseAgent):
    agent_name = "voice"
    model_env_var = "VOICE_MODEL"
    default_temperature = 0.4
    default_max_tokens = 2000

    system_prompt = (
        "Ты — VOICE, голосовой агент платформы AI Zavod. "
        "Готовишь скрипты для телефонных звонков: деловые переговоры, "
        "запись на приём, уточнение информации, продажи. "
        "Скрипты должны звучать естественно при озвучке через TTS. "
        "Два режима: 'мой голос' (деловые, от имени Азата) и "
        "'универсальный' (рутинные). Учитывай российский контекст."
    )

    async def business_call_script(self, context: str, goal: str) -> str:
        """Скрипт делового звонка."""
        prompt = f"""Подготовь скрипт делового звонка.

КОНТЕКСТ: {context}
ЦЕЛЬ ЗВОНКА: {goal}

Структура:
1. **Приветствие** — представление, причина звонка
2. **Основная часть** — суть вопроса/предложения
3. **Возражения** — 3-5 типичных и ответы на них
4. **Закрытие** — фиксация договорённостей
5. **Прощание** — следующий шаг

Требования:
- Естественная речь (не роботизированная)
- Краткие фразы (для TTS)
- Паузы обозначены [пауза 1с]
- Варианты ответов на реплики собеседника
- Время звонка: до 3 минут"""
        return await self._call_llm(prompt)

    async def routine_call_script(self, task: str) -> str:
        """Скрипт рутинного звонка (запись, справка)."""
        prompt = f"""Подготовь скрипт рутинного звонка.

ЗАДАЧА: {task}

Структура:
1. **Приветствие** — короткое, вежливое
2. **Запрос** — чётко сформулированный вопрос
3. **Уточнения** — если нужна дополнительная информация
4. **Фиксация** — повтор ключевой информации
5. **Прощание** — благодарность

Требования:
- Максимально короткие фразы
- Естественный тон
- Готовность к переключению между операторами
- Паузы для ожидания ответа: [ожидание]"""
        return await self._call_llm(prompt, max_tokens=1500)

    async def sales_script(self, product: str, target: str) -> str:
        """Скрипт продающего звонка."""
        prompt = f"""Подготовь скрипт продающего звонка.

ПРОДУКТ: {product}
ЦЕЛЕВАЯ АУДИТОРИЯ: {target}

Структура:
1. **Открытие** — привлечь внимание за 10 секунд
2. **Квалификация** — подходит ли клиент
3. **Презентация** — боль → решение → выгода
4. **Кейс** — конкретный пример
5. **Предложение** — что конкретно предлагаем
6. **Возражения** (5 типичных):
   - "Дорого"
   - "Подумаю"
   - "Уже есть"
   - "Не интересно"
   - "Пришлите на почту"
7. **Закрытие** — назначить следующий шаг

Требования:
- Не агрессивный, но настойчивый
- Конкретные цифры и выгоды
- Время: до 5 минут"""
        return await self._call_llm(prompt, max_tokens=3000)

    async def tts_optimize(self, text: str) -> str:
        """Оптимизировать текст для озвучки через TTS."""
        prompt = f"""Оптимизируй текст для озвучки через TTS (text-to-speech).

ТЕКСТ:
{text}

Сделай:
1. Замени аббревиатуры на полные слова
2. Числа — словами
3. Разбей длинные предложения на короткие
4. Добавь паузы [пауза] где нужно
5. Расставь ударения для сложных слов: слОво
6. Убери то, что плохо звучит при озвучке

Верни оптимизированный текст, готовый к TTS."""
        return await self._call_llm(prompt, max_tokens=2000)


_agent: VoiceAgent | None = None


def get_voice_agent() -> VoiceAgent:
    global _agent
    if _agent is None:
        _agent = VoiceAgent()
    return _agent
